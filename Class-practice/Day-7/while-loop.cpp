#include <iostream>

using namespace std;

int main() {
    int i = 1;

    // While Loop Vartical
    while (i <= 5){
        cout << i << endl;
        i++;
    }

    cout << i << endl;


    // While Loop Horizontal
    while (i <= 5){
        cout << i << " ";
        i++;
    }

    cout << i ;
    

    return 0;
}