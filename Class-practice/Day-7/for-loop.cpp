#include <iostream>

using namespace std;

int main() {
    int i;

    // For Loop Vartical
    
    for (i = 1; i <= 5; i++)
    {
        cout << i << endl;
    }
    
    cout << i << endl;


    // For Loop Horizontal
    
    for (i = 1; i <= 5; i++)
    {
        cout << i << " ";
    }
    
    cout << i;
    

    return 0;
}